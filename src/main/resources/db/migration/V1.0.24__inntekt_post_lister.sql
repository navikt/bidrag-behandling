CREATE TABLE IF NOT EXISTS INNTEKT_POST (
    ID INT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY (INCREMENT 1 START 1 MINVALUE 1),
    INNTEKT_ID      INT NOT NULL,

    BELOP           DECIMAL NOT NULL DEFAULT 0,
    kode            TEXT,
    visningsnavn    TEXT,

    CONSTRAINT FK_INNTEKT_ID FOREIGN KEY (INNTEKT_ID)
    REFERENCES INNTEKT (ID) MATCH SIMPLE
)